#include <stdio.h>
#include <limits.h>
#include <stdint.h>
#include <stdlib.h>

/*
 * = atribuicao
 * + - unarios
 * + - binarios (adicao e subtracao)
 * * / (multiplicacao e divisao)
 * % (resto da divisao)
 * ++ (incremento)
 * -- (decremento)
*/

int main(void) {
  int a;

  a = 18;
  printf("%d\n", a--);

  printf("%d\n", a);


  return 0;
}
